# Tipos de dados
# Em Python, existem vários tipos de dados que podemos usar para armazenar diferentes tipos de informações. Alguns dos tipos de dados mais comuns incluem:
# 1. Números: Podemos usar os tipos de dados int (inteiros) e float (números decimais) para armazenar valores numéricos.
# Exemplo de números    
idade = 25  # variável do tipo int
preco = 19.99  # variável do tipo float
print("Idade:", idade)
print("Preço:", preco)
# 2. Strings: O tipo de dado str é usado para armazenar texto. As strings são delimitadas por aspas simples (' ') ou aspas duplas (" ").
# Exemplo de strings    
nome = "Alice"  # variável do tipo str
sobrenome = 'Smith'  # variável do tipo str 
print("Nome:", nome)
print("Sobrenome:", sobrenome)
# 3. Listas: O tipo de dado list é usado para armazenar uma coleção de itens. As listas são delimitadas por colchetes [ ] e os itens são separados por vírgulas.
# Exemplo de listas
frutas = ["maçã", "banana", "laranja"]  # variável do tipo list
print("Frutas:", frutas)
# 4. Dicionários: O tipo de dado dict é usado para armazenar pares de chave-valor. Os dicionários são delimitados por chaves { } e os pares de chave-valor são separados por vírgulas.
# Exemplo de dicionários
pessoa = {"nome": "Alice", "idade": 30, "cidade": "São Paulo"}  # variável do tipo dict
print("Pessoa:", pessoa)
# 5. Booleanos: O tipo de dado bool é usado para armazenar valores de verdade, que podem ser True (verdadeiro) ou False (falso).
# Exemplo de booleanos
is_student = True  # variável do tipo bool
print("É estudante?", is_student)   
