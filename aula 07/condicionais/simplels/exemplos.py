# Condicionais simples
# Em Python, as estruturas condicionais permitem que o programa tome decisões com base em certas condições. A estrutura condicional mais simples é a declaração if, que executa um bloco de código se uma condição for verdadeira. Aqui estão alguns exemplos de condicionais simples:
# Exemplo 1: Verificar se um número é positivo
numero = 10
if numero > 0:
    print("O número é positivo.")
# Exemplo 2: Verificar se um número é par
numero = 4
if numero % 2 == 0:
    print("O número é par.")
# Exemplo 3: Verificar se um nome é "Alice"
nome = "Alice"
if nome == "Alice":
    print("Olá, Alice!")
# Exemplo 4: Verificar se um número é maior que 100
numero = 150
if numero > 100:
    print("O número é maior que 100.")
# Exemplo 5: Verificar se um número é negativo
numero = -5
if numero < 0:
    print("O número é negativo.")
    