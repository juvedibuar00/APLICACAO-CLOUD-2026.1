# Operadores aritméticos
a = 10
b = 5
print("Soma:", a + b)  # Adição
print("Subtração:", a - b)  # Subtração
print("Multiplicação:", a * b)  # Multiplicação
print("Divisão:", a / b)  # Divisão
print("Divisão inteira:", a // b)  # Divisão inteira
print("Resto da divisão:", a % b)  # Resto da divisão
print("Exponenciação:", a ** b)  # Exponenciação
# Operadores de comparação
print("a é igual a b?", a == b)  # Igualdade
print("a é diferente de b?", a != b)  # Diferença
print("a é maior que b?", a > b)  # Maior que
print("a é menor que b?", a < b)  # Menor que
print("a é maior ou igual a b?", a >= b)  # Maior ou igual a
print("a é menor ou igual a b?", a <= b)  # Menor ou igual a
# Operadores lógicos
x = True
y = False
print("x AND y:", x and y)  # AND lógico
print("x OR y:", x or y)  # OR lógico
print("NOT x:", not x)  # NOT lógico
