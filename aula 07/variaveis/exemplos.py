# O que são variáveis?
# Variáveis são espaços na memória do computador onde podemos armazenar dados temporariamente. Elas permitem que possamos manipular e utilizar esses dados em nossos programas. Em Python, as variáveis são criadas automaticamente quando você atribui um valor a elas.
# Exemplo de criação e uso de variáveis em Python:
# Criando variáveis
nome = "Alice"
idade = 30
# Usando as variáveis
print("Nome:", nome)
print("Idade:", idade)
# Podemos também realizar operações com as variáveis
soma = idade + 5
print("Idade daqui a 5 anos:", soma)
# Variáveis podem armazenar diferentes tipos de dados, como números, texto, listas, etc.
# Exemplo com diferentes tipos de dados
preco = 19.99  # variável do tipo float
frutas = ["maçã", "banana", "laranja"]  # variável do tipo lista
print("Preço:", preco)
print("Frutas:", frutas)

